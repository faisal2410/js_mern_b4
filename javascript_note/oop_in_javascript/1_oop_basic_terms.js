// Here are some basic terms of Object - Oriented Programming(OOP) in JavaScript:

// Class: A blueprint for creating objects that defines its properties and methods.

// Object: An instance of a class that contains its own set of properties and methods.

// Constructor: A special method that is used to create and initialize an object created from a class.

// Inheritance: The ability of an object to inherit properties and methods from its parent class.

// Encapsulation: The practice of hiding the implementation details of a class from the outside world, and only exposing a public interface.

// Abstraction: The practice of simplifying complex systems by only showing the necessary details to the user.

// Polymorphism: The ability of an object to take on many forms or types, depending on the context in which it is used.

// Method: A function that is a property of an object.

// Property: A named value that is associated with an object.

// this: A keyword that refers to the current object that is being executed in the code.

// Instance: An object that is created from a class.

// Prototype: An object that serves as a template for other objects to inherit properties and methods.

// Prototype Chain: The mechanism that allows objects to inherit properties and methods from their prototypes.

// Method Chaining: A technique where multiple methods can be chained together in a single statement, with each method returning the object for the next method to use.

// Private and Public Members: Private members are not accessible outside of the class, while public members can be accessed by any code that has access to the object.

// Static Members: Members that are associated with a class rather than with an instance of the class.

// Getters and Setters: Special methods that allow reading and writing of the private members of an object.

// Interface: A set of methods and properties that defines the behavior of an object.

// Mixin: A technique where multiple classes are combined to create a new class with the properties and methods of all the classes.

// Constructor Function: A function that creates an object, used in earlier versions of JavaScript before the introduction of the class syntax.

// Factory Function: A function that creates and returns an object without the use of the new keyword or a class constructor.

// Polymorphic Method: A method that can take different types of parameters and behave differently depending on the type of parameter it receives.

// Prototype - based Inheritance: A type of inheritance where objects inherit directly from other objects, rather than from classes.

// Object Literal: A way to create an object by specifying its properties and methods in a single expression, using curly braces { }.

// Super Keyword: A keyword that is used to call a method in the parent class from a method in the child class.

// Class Inheritance: A type of inheritance where a child class inherits properties and methods from a parent class.

// Abstract Class: A class that cannot be instantiated directly, but can be used as a base class for other classes.

// Composition: A technique where objects are composed of other objects, rather than being created by inheritance.

// Mixin Function: A function that adds properties and methods to an object without the need for inheritance.

// Polymorphic Interface: An interface that is implemented by multiple classes, each with their own implementation of the methods defined in the interface.


// Arrow Function: A shorthand syntax for creating functions that preserve the this keyword from the surrounding context.

// Singleton: A design pattern that restricts the instantiation of a class to a single object, ensuring there is only one instance of the class.

// Composition over Inheritance: A design principle that suggests that code reuse should be achieved through composition of smaller, simpler objects rather than inheritance from larger, more complex objects.

// Decorator: A design pattern that allows behavior to be added to an individual object, either statically or dynamically, without affecting the behavior of other objects from the same class.

// Mixin Object: An object that contains a set of methods and properties that can be added to another object to extend its functionality.

// Proxy Object: An object that intercepts operations performed on another object and provides a way to customize or augment their behavior.

// Duck Typing: A type system where the type of an object is determined by the presence of its methods and properties, rather than its class or interface.

// Inversion of Control: A design principle where the control of the flow of a program is inverted, with the framework or library controlling the execution flow of the code.

// Callback Function: A function that is passed as an argument to another function and is executed when a certain event occurs.

// Higher - Order Function: A function that takes another function as an argument or returns a function as its result.

// Promise: A built -in object that represents a value that may not be available yet, but will be resolved at some point in the future.

// Async / await: A syntax for working with Promises that makes asynchronous code look and behave like synchronous code.

// Generator: A function that can be paused and resumed, allowing for the creation of iterators.

// Iterator: An object that provides a sequence of values, typically used to iterate over collections such as arrays or maps.

// Spread Operator: A syntax for expanding an iterable(such as an array or object) into individual elements.

// Destructuring: A syntax for extracting values from arrays or objects and assigning them to variables.

// Template Literal: A syntax for creating strings that allows for embedded expressions and multiline strings.

// Arrow Function: A shorthand syntax for creating functions that preserve the this keyword from the surrounding context.

// Closure: A function that retains access to the variables in its outer scope, even after the outer function has completed execution.

// Currying: A technique where a function that takes multiple arguments is transformed into a series of functions that take one argument each, allowing for partial application of the function.